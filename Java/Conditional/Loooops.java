package Java.Conditional;
public class Loooops {
    public static void main(String[] args) throws Exception {
        int contadorw = 1;
        int contadordo = 100;

        while (contadorw > 0){
            System.out.println(contadorw);
            contadorw--;
        }
        do {
            System.out.println(contadordo);
            contadorw--;
        } while (contadordo > 100);
    }
}
